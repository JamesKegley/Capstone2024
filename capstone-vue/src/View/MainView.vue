<template>
    <v-alert v-model="successAlert" type="success" :text="alertText" closable>
    </v-alert>


    <v-container class="mt-16">
        <v-row>
            <!--Table View-->
            <v-col cols="12">
                <v-data-table
                    :headers = "CurrentTableHeaders"
                    :items = "personList"
                    :row-props="colorSelectedRow"
                    v-model="selectedRow"
                    return-object
                    select-strategy="single"
                    item-value="id"
                    :show-select="showSelected">
                
                    <template v-slot:top>
                        <v-toolbar
                            :color="'red-lighten-1'"
                            :rounded="true"
                            dark
                        >
                            <v-toolbar-title :style="'text-align: left'">{{ currentTableView }}</v-toolbar-title>

                            <v-spacer></v-spacer>


                            <v-btn v-show="currentTableView==='Person'" class="mx-auto" @click="toggleCreatePersonDialog()">Create Person</v-btn>
                        </v-toolbar>
                    </template>
                </v-data-table>
            </v-col>
            
            <!--Info Form-->
            <v-col style="display: none" id="InfoSection">
                <v-card outlined color="grey-lighten-3" class="section-container" elevation="2" flex>
                    <v-card-title :style="'text-align: left'">Information</v-card-title>
                        <v-card-text>
                            <v-form>
                                <v-row >
                                    <v-col cols="2">
                                        <!-- Placeholder for picture -->
                                        <img src="placeholder.jpg" alt="Placeholder" class="picture"/>
                                    </v-col>
                                    <v-row>
                                        <!-- Text boxes -->
                                        <v-col cols="3">
                                            <v-text-field variant="underlined" label="First Name" v-model="personSelectedObj.fName"></v-text-field>
                                            <v-text-field variant="underlined" label="ID" v-model="personSelectedObj.id"></v-text-field>
                                            <v-text-field variant="underlined" label="Access" v-model="personSelectedObj.accessCode"></v-text-field>
                                        </v-col>
                                        <v-col cols="3">
                                            <v-text-field variant="underlined" label="Last Name" v-model="personSelectedObj.lName"></v-text-field>
                                            <v-text-field variant="underlined" label="CID" v-model="personSelectedObj.cid"></v-text-field>
                                        </v-col>
                                        <v-col cols="3">
                                            <v-text-field variant="underlined" label="Email" v-model="personSelectedObj.email"></v-text-field>
                                            <v-text-field variant="underlined" label="Title" v-model="personSelectedObj.title"></v-text-field>
                                        </v-col>
                                    </v-row>
                                </v-row>
                            </v-form>
                        </v-card-text>
                    <v-card-actions>
                    <!-- Save and Delete buttons -->
                    <v-btn @click="saveData" color="primary">Save</v-btn>
                    <v-btn @click="deleteData" color="error">Delete</v-btn>
                    </v-card-actions>
                </v-card>
            </v-col>
        </v-row>
    </v-container>

</template>

<script>
import {personApi} from '../service/person.api.js'

    export default {
        data() {
            return {
                //Table Headers
                CurrentTableHeaders: [],
                PersonTableHeaders: [
                    {title: "ID", key: "id"},
                    {title: "First Name", key: "fName"},
                    {title: "Last Name", key: "lName"},
                    {title: "Email", key: "email"},
                    {title: "Title", key: "title"},
                    {title: "CID", key: "cid"},
                    {title: "Access", key: "accessCode"},
                ],
                RequestLogTableHeaders: [
                    {title: "ID", key: "id"},
                    {title: "Lock", key: "lockNum"},
                    {title: "Access", key: "accessCode"},
                    {title: "Date Requested", key: "dateRequested"},
                    {title: "Date Approved", key: "dateApproved"},
                    {title: "Advisor ID", key: "advisorID"},
                ],
                AccessLogTableHeaders: [
                    {title: "ID", key: "id"},
                    {title: "Lock", key: "lockNum"},
                    {title: "Time Accessed", key: "accessTime"},
                    {title: "CID", key: "cid"},
                    {title: "Accepted", key: "accepted"},
                ],
                currentTableView: "Person",
                selection: [],
                selectedRow: [],

                //DataBase Model Objects
                personSelectedObj: {
                    id: "",
                    fName: "",
                    lName: "",
                    email: "",
                    title: "",
                    cid: "",
                    accessCode: ""
                },
                personObj: {
                    id: "",
                    fName: "",
                    lName: "",
                    email: "",
                    title: "",
                    cid: "",
                    accessCode: ""
                },
                requestLogObj: {},
                accessLogObj: {},

                //Create Person Variables
                showCreatePersonDialog: false,

                //Navigation Variables


                //Alert Variables
                successAlert: false,
                alertText: "",

                //DataBase Return Lists
                accessLogList: [],
                requestLogList: [],
                personList: [],

                showSelected: true,

                //Dropdowns for title and access on create person form
                selectedItem: null,
                    dropdownItems: [
                        'WKD',
                    ],

                selectedItem2: null,
                    dropdownItems2: [
                        'Student',
                        'Guest'
                    ],

            }
        },
        methods: {

            async GetAllPersons() {
                await personApi.getAllPersons().then(response => {
                    this.personList = response;
                    this.CurrentTableHeaders = this.PersonTableHeaders;
                });
            },
            async InsertPersonInfo(){
                await personApi.insertPersonInfo(this.personObj).then(response => {
                    if(response === 'Success'){
                        this.alertText = `${this.personObj.fName} ${this.personObj.lName} was successfully created`
                        this.successAlert = true;
                        this.toggleCreatePersonDialog();
                        this.LoadTable();
                        this.setAlertTimeOut();
                    }
                    else{
                        this.alertText = `${this.personObj.fName} ${this.personObj.lName} failed to created`
                    }
                })
            },
            LoadTable(){
                this.GetAllPersons();
            },
            colorSelectedRow(row) {
                if(row.item === this.selectedRow[0]){
                    return {style: 'background: #42A5F5; color: white;'};
                }
            },
            toggleCreatePersonDialog(){
                if(this.showCreatePersonDialog === false){
                    this.showCreatePersonDialog = true;
                }
                else{
                    this.showCreatePersonDialog = false;
                }
            },
            setAlertTimeOut(){
                setTimeout(() =>{
                    this.successAlert=false
                }, 3000)
            }
        },
        watch: {
            selectedRow(){
                if(this.currentTableView === 'Person'){
                    document.getElementById("InfoSection").style.display = "none";
                    if(this.selectedRow.length < 1){
                        this.personSelectedObj = {
                            id: "",
                            fName: "",
                            lName: "",
                            email: "",
                            title: "",
                            cid: "",
                            accessCode: ""
                        }
                    }
                    else{
                        document.getElementById("InfoSection").style.display = "block";
                        this.personSelectedObj = this.selectedRow[0]
                    }
                }
            }
        },
        async beforeMount() {
            this.LoadTable();
        },
        created(){

        }
        
    }
</script>

